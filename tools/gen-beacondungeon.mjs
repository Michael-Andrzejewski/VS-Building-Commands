const R = (x, z) => Math.sqrt(x * x + z * z);

// vertical wall columns for a circular band rIn..rOut (per lattice cell)
function ringWall(rIn, rOut, y1, y2, matFn, skipFn) {
    const m = Math.ceil(rOut);
    for (let x = -m; x <= m; x++) for (let z = -m; z <= m; z++) {
        const r = R(x, z);
        if (r < rIn || r >= rOut) continue;
        if (skipFn && skipFn(x, z)) continue;
        fill(x, y1, z, x, y2, z, matFn());
    }
}
// horizontal disc/annulus as per-row fills at a single y (layers via loop)
function annulus(y, rIn, rOut, block) {
    const m = Math.ceil(rOut);
    for (let z = -m; z <= m; z++) {
        const half = rOut * rOut - z * z;
        if (half < 0) continue;
        const xo = Math.floor(Math.sqrt(half));
        const innerHalf = rIn * rIn - z * z;
        if (innerHalf <= 0) { fill(-xo, y, z, xo, y, z, block); continue; }
        const xi = Math.ceil(Math.sqrt(innerHalf));
        if (xi > xo) continue;
        fill(-xo, y, z, -xi, y, z, block);
        fill(xi, y, z, xo, y, z, block);
    }
}
const disc = (y, r, block) => annulus(y, 0, r, block);

