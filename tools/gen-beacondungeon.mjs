function sb(x, y, z, block) { target.push(`setblock ${t(x)} ${t(y + YOFF)} ${t(z)} ${block}`); }
function chest(x, y, z, count) { target.push(`lootchest ${t(x)} ${t(y + YOFF)} ${t(z)} ${count} ${pick(['north', 'south', 'east', 'west'])}`); }
