// cutaway: drop everything at z < 0 so the interior is reviewable
function cutLines(all) {
    const cut = [];
    for (const line of all) {
        if (line.startsWith('#')) { cut.push(line); continue; }
        const p = line.split(/\s+/);
        const num = (tok) => (tok[0] === '~' ? parseInt(tok.slice(1) || '0', 10) : parseInt(tok, 10)) || 0;
        const cmd = p[0];
        if (cmd === 'fill') {
            let z1 = num(p[3]), z2 = num(p[6]);
            if (Math.max(z1, z2) < 0) continue;
            z1 = Math.max(z1, 0); z2 = Math.max(z2, 0);
            cut.push(`fill ${p[1]} ${p[2]} ${t(Math.min(z1, z2))} ${p[4]} ${p[5]} ${t(Math.max(z1, z2))} ${p.slice(7).join(' ')}`);
        } else {
            if (num(p[3]) < 0) continue;
            cut.push(line);
        }
    }
    return cut;
}

