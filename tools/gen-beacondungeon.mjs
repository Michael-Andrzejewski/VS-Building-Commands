const roomTypes = ['lab', 'archive', 'storage', 'reactor', 'quarters', 'breached'];
let roomIndex = 0;

function buildRoom(cx, cz, w, hh, d, floorY) {
    const type = roomTypes[roomIndex++ % roomTypes.length];
    const x1 = cx - Math.floor(w / 2), x2 = cx + Math.floor(w / 2);
    const z1 = cz - Math.floor(d / 2), z2 = cz + Math.floor(d / 2);
    const yTop = floorY + hh + 1;
    fill(x1, floorY, z1, x2, floorY, z2, type === 'breached' ? DROCK : PLATE);   // floor
    fill(x1, yTop, z1, x2, yTop, z2, MET_P);                                     // ceiling
    fill(x1, floorY + 1, z1, x1, yTop - 1, z2, brickMat());                      // walls
    fill(x2, floorY + 1, z1, x2, yTop - 1, z2, brickMat());
    fill(x1, floorY + 1, z1, x2, yTop - 1, z1, brickMat());
    fill(x1, floorY + 1, z2, x2, yTop - 1, z2, brickMat());
    fill(x1 + 1, floorY + 1, z1 + 1, x2 - 1, yTop - 1, z2 - 1, 'air');           // interior
    // door opening on the wall facing the world center, along the dominant axis
    const axisX = Math.abs(cx) >= Math.abs(cz);
    const doorY2 = floorY + 4;
    if (axisX) {
        const wx = cx > 0 ? x1 : x2;
        fill(wx, floorY + 1, cz - 1, wx, doorY2, cz + 1, 'air');
    } else {
        const wz = cz > 0 ? z1 : z2;
        fill(cx - 1, floorY + 1, wz, cx + 1, doorY2, wz, 'air');
    }
    sb(cx, yTop - 1, cz, GLOW);
    const fy = floorY + 1;
    if (type === 'lab') {
        fill(x1 + 2, fy, z1 + 2, x2 - 2, fy, z1 + 2, MET_P);
        fill(x1 + 2, fy, z2 - 2, x2 - 2, fy, z2 - 2, MET_P);
        sb(x1 + 3, fy + 1, z1 + 2, 'game:lootvessel-seed'); sb(x2 - 3, fy + 1, z2 - 2, 'game:lootvessel-food');
        chest(cx + 1, fy, cz, 2);
    } else if (type === 'archive') {
        for (let zz = z1 + 2; zz <= z2 - 2; zz += 3) fill(x1 + 2, fy, zz, x2 - 2, fy + 2, zz, PLANKS);
        chest(cx, fy, cz, 3);
    } else if (type === 'storage') {
        ingots(x1 + 2, fy, z1 + 2, 'copper', 20 + Math.floor(rnd() * 20));
        ingots(x2 - 2, fy, z2 - 2, 'copper', 20 + Math.floor(rnd() * 20));
        sb(x1 + 2, fy, z2 - 2, 'game:lootvessel-ore'); sb(x2 - 2, fy, z1 + 2, 'game:lootvessel-tool');
        chest(cx, fy, cz, 4); chest(cx + 2, fy, cz + 1, 3);
    } else if (type === 'reactor') {
        fill(cx - 1, fy, cz - 1, cx + 1, fy + 2, cz + 1, MET_R);
        sb(cx, fy + 3, cz, GLOW);
        scatter(x1 + 2, fy, z1 + 2, pick(GEARS)); scatter(x2 - 2, fy, z2 - 2, pick(GEARS));
        chest(x1 + 2, fy, z2 - 2, 3);
    } else if (type === 'quarters') {
        fill(x1 + 3, fy, z1 + 1, x1 + 3, fy + 2, z1 + 3, PLANKS);
        fill(x2 - 3, fy, z2 - 1, x2 - 3, fy + 2, z2 - 3, PLANKS);
        sb(x1 + 1, fy, z1 + 1, 'game:lootvessel-farming'); chest(x2 - 1, fy, z2 - 1, 2);
    } else { // breached
        scatter(cx - 1, fy, cz + 1, COBBLE, 3); scatter(cx + 2, fy, cz - 2, DROCK, 2);
        sb(x1 + 2, fy, z1 + 2, pick(GROWTHS)); sb(x2 - 2, fy, z2 - 2, pick(GROWTHS));
        sb(cx + 1, fy, cz + 2, DSOIL); sb(x1 + 1, fy, z2 - 2, SKULL);
        chest(cx - 2, fy, cz - 1, 2);
    }
    return { x1, x2, z1, z2, axisX };
}

// straight enclosed passage from a room to its corridor ring (dominant axis)
function passage(cx, cz, floorY, room, corrOuterR) {
    const axisX = room.axisX;
    const dir = axisX ? (cx > 0 ? -1 : 1) : (cz > 0 ? -1 : 1);
    let px = axisX ? (cx > 0 ? room.x1 : room.x2) : cx;
    let pz = axisX ? cz : (cz > 0 ? room.z1 : room.z2);
    let guard = 0;
    while (R(px, pz) > corrOuterR - 1 && guard++ < 40) {
        if (axisX) {
            fill(px, floorY, pz - 2, px, floorY, pz + 2, PLATE);
            fill(px, floorY + 5, pz - 2, px, floorY + 5, pz + 2, MET_P);
            fill(px, floorY + 1, pz - 2, px, floorY + 4, pz - 2, brickMat());
            fill(px, floorY + 1, pz + 2, px, floorY + 4, pz + 2, brickMat());
            fill(px, floorY + 1, pz - 1, px, floorY + 4, pz + 1, 'air');
            px += dir;
        } else {
            fill(px - 2, floorY, pz, px + 2, floorY, pz, PLATE);
            fill(px - 2, floorY + 5, pz, px + 2, floorY + 5, pz, MET_P);
            fill(px - 2, floorY + 1, pz, px - 2, floorY + 4, pz, brickMat());
            fill(px + 2, floorY + 1, pz, px + 2, floorY + 4, pz, brickMat());
            fill(px - 1, floorY + 1, pz, px + 1, floorY + 4, pz, 'air');
            pz += dir;
        }
    }
}

function ringLevel(name, floorY, rIn, rOut, roomR, roomCount, roomW, roomH, roomD, angleOffset = 0) {
    lines.push('# ' + name);
    const ceilY = floorY + 7;
    annulus(floorY, rIn - 0.5, rOut + 0.5, PLATE);
    annulus(ceilY, rIn - 0.5, rOut + 0.5, MET_P);
    ringWall(rOut, rOut + 1.5, floorY + 1, ceilY - 1, brickMat);
    ringWall(rIn - 1.5, rIn, floorY + 1, ceilY - 1, brickMat);
    fillRingAir(floorY + 1, ceilY - 1, rIn, rOut);
    // corridor lights
    for (let a = 0; a < 12; a++) {
        const rr = (rIn + rOut) / 2;
        sb(Math.round(rr * Math.cos(a * Math.PI / 6)), ceilY - 1, Math.round(rr * Math.sin(a * Math.PI / 6)), GLOW);
    }
    // rooms around the ring
    for (let i = 0; i < roomCount; i++) {
        const a = angleOffset + i * 2 * Math.PI / roomCount + (rnd() * 0.1 - 0.05);
        const cx = Math.round(roomR * Math.cos(a)), cz = Math.round(roomR * Math.sin(a));
        const room = buildRoom(cx, cz, roomW, roomH, roomD, floorY);
        passage(cx, cz, floorY, room, rOut);
    }
}
// carve the corridor ring interior (annulus of air per row)
function fillRingAir(y1, y2, rIn, rOut) {
    const m = Math.ceil(rOut);
    for (let z = -m; z <= m; z++) {
        const half = rOut * rOut - z * z;
        if (half < 0) continue;
        const xo = Math.floor(Math.sqrt(half));
        const innerHalf = rIn * rIn - z * z;
        if (innerHalf <= 0) { fill(-xo, y1, z, xo, y2, z, 'air'); continue; }
        const xi = Math.ceil(Math.sqrt(innerHalf));
        if (xi > xo) continue;
        fill(-xo, y1, z, -xi, y2, z, 'air');
        fill(xi, y1, z, xo, y2, z, 'air');
    }
}

