// blade slices + beam per spire height; consumes no randomness, so both
// variants share the identical body emitted above
function spireLines(bladeTop, beamTop) {
    const out = [];
    const prev = target;
    target = out;
    // the beacon beam, rising between the blades (overwrites one roof cell)
    fill(0, 122, 0, 0, beamTop, 0, GLOW);
    for (let yy = 106; yy <= bladeTop; yy++) {
        const tt = (yy - 106) / (bladeTop - 106);
        const xc = (16 - 11 * tt) + 19 * Math.pow(Math.sin(Math.PI * tt), 1.2);
        const th = Math.max(1, Math.round((6 - 4 * tt) / 2));       // half thickness in x
        const hw = Math.max(1, Math.round((13 - 9 * tt) / 2));      // half width in z
        const body = (yy % 10 < 2) ? MET_R : MET_P;
        for (const s of [1, -1]) {
            const cx = Math.round(s * xc);
            fill(cx - th, yy, -hw, cx + th, yy, hw, body);
            // glowing energy edge on the inner face
            const inner = cx - s * (th + 1);
            fill(inner, yy, -Math.max(1, hw - 1), inner, yy, Math.max(1, hw - 1), GLOW);
        }
    }
    target = prev;
    return out;
}

